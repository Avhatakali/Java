package academy.learnProgramming;

public class person {

    //what does a person have 'variables describing a person'
    private String firstName;
    private String lastName;
    private int age;

    //getter methods have return values
    private String getFirstName(){
        return firstName;
    }

    private String getLastName(){  //method signature
        return lastName;
    }

    private int getAge(){
        return age;
    }
}
